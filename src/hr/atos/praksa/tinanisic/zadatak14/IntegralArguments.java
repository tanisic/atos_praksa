package hr.atos.praksa.tinanisic.zadatak14;

public class IntegralArguments {
	
	private double t1,t2,A,B;
	
	public IntegralArguments(double t1,double t2,double A,double B) {
		this.t1=t1;
		this.t2=t2;
		this.A=A;
		this.B=B;
	}

	public double getT1() {
		return t1;
	}

	public void setT1(double t1) {
		this.t1 = t1;
	}

	public double getT2() {
		return t2;
	}

	public void setT2(double t2) {
		this.t2 = t2;
	}

	public double getA() {
		return A;
	}

	public void setA(double a) {
		A = a;
	}

	public double getB() {
		return B;
	}

	public void setB(double b) {
		B = b;
	}
	
	
	

	
	
}
